package Day_12;

public class Account 
{

	private int acno;
	private String name;
	private double amount;
	
	public int getAcno() 
	{
		return acno;
	}
	
	
	public void setAcno(int acno) 
	{
		this.acno = acno;
	}
	
	
	public String getName() 
	{
		return name;
	}
	
	
	public void setName(String name) 
	{
		this.name = name;
	}
	
	
	public double getAmount() 
	{
		return amount;
	}
	
	
	public void setAmount(double amount) 
	{
		this.amount = amount;
	}
	
	
	
	
	
}

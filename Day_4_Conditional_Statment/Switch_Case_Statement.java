package Day_4_Conditional_Statment;

public class Switch_Case_Statement {

	public static void main(String[] args) {
		
		int weekno=0;
		
		switch(weekno)
		{
		
		case 1: System.out.println("Monday"); break;
		case 2: System.out.println("tuesday"); break;
		case 3: System.out.println("wedneday"); break;
		case 4: System.out.println("thusday"); break;
		case 5: System.out.println("friday"); break;
		case 6: System.out.println("saturday"); break;
		case 7: System.out.println("sunday funday"); break;
		default: System.out.println("invalid week no");
		}
				

	}

}

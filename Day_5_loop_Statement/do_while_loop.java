package Day_5_loop_Statement;

public class do_while_loop {

	public static void main(String[] args) {

		//1.....10  numbers
		 int i=1;
		 do
		 {
			 System.out.println(i);
			 i++;
		 }
		 while(i<=10);

		 //10 9 .....1
		 
		 int x=10;
		 do
		 {
			 System.out.println(x);
			 x--;
		 }
		 while(x>0);
		 
	}

}

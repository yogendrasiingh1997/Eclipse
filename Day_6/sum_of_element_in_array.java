package Day_6;

public class sum_of_element_in_array {

	public static void main(String[] args) {
		
		int a[]= {12,10,8,20,30};
		int sum=0;
		
		for (int i=0;i<a.length;i++)
		{
			sum=sum+a[i];
			
		}
		
		System.out.println(sum);
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}

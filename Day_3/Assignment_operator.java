package Day_3;

public class Assignment_operator {

	public static void main(String[] args) {
		
		//Assignment operator = += -= *= /= %=
		
		
		//case 1
		int a=10;
		a+=5;
		System.out.println(a);
	    
		
		//case 2
		int b=10;
		b-=5;
		System.out.println(b);

		
		//case 3
		
		int c=2;
		c*=2;
		System.out.println(c);
		
		//case 4
		int d=20;
		d/=4;
		System.out.println(d);
				
	}

}
